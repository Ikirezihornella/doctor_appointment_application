class Appointment {
  final String firstName;
  final String lastName;
  final int age;
  final String address;
  final String sicknessRecognizedAt;
  final String mobile;
  final String email;

  Appointment({
    required this.firstName,
    required this.lastName,
    required this.age,
    required this.address,
    required this.sicknessRecognizedAt,
    required this.mobile,
    required this.email,
  });
}
